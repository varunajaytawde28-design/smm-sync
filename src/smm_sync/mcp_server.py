def _time_saved_footer() -> str:
    """Build a one-line time saved footer for MCP tool responses.

    Reads compliance_lineage.jsonl for injection count in the last 7 days.
    Formula: injections × 3.75 min.

    Returns:
        Footer string to append to tool responses.
    """
    try:
        import json as _json
        from datetime import datetime, timedelta, timezone

        smm_dir = _smm_dir
        if smm_dir is None:
            return ""
        lineage_path = smm_dir / "compliance_lineage.jsonl"
        if not lineage_path.exists():
            return ""
        cutoff = datetime.now(timezone.utc) - timedelta(days=7)
        count = 0
        with open(lineage_path, encoding="utf-8") as f:
            for line in f:
                line = line.strip()
                if not line:
                    continue
                try:
                    entry = _json.loads(line)
                    if entry.get("event_type") != "context_injection":
                        continue
                    ts_str = entry.get("timestamp", "")
                    if ts_str:
                        ts = datetime.fromisoformat(ts_str.replace("Z", "+00:00"))
                        if ts >= cutoff:
                            count += 1
                except Exception:
                    continue
        mins_week = int(count * 3.75)
        h, m = divmod(mins_week, 60)
        week_str = f"~{h}h {m}m" if h > 0 else f"~{m}m"
        return (
            f"\n\n---\n"
            f"⏱ CaaS: saved ~3.75 min · Total this week: {week_str}"
        )
    except Exception:
        return ""


@mcp.tool(structured_output=False)
async def query_decisions(
    query: str,
    project: str = "smm-sync",
    limit: int = 5,
    session_id: str = "",
) -> str:
    """Search team decisions and architectural knowledge.

    Call this when you need to understand WHY something was built a certain way,
    or what constraints exist before making changes.

    Args:
        query: Natural language question about the project.
               e.g. "why did we choose Kuzu?"
               e.g. "what are the constraints on the MCP server?"
               e.g. "what alternatives were considered for file locking?"
        project: Project name (default: smm-sync).
        limit: Max results to return (default: 5).
        session_id: Optional session identifier (used for kill check).

    Returns:
        Formatted string of relevant decisions with rationale.
    """
    global _context_loaded
    if not _context_loaded:
        return _SESSION_NOT_INIT_ERROR
    if session_id and _is_session_killed(session_id):
        return _KILLED_MESSAGE
    client = _get_graph_client()
    if client is None:
        return "Context graph unavailable. Run `smm seed-graph` to populate it."

    try:
        results = await client.search_context(query=query, project=project, limit=limit)
    except Exception as e:
        return f"Graph search failed: {e}"

    # Log to compliance lineage
    logger = _get_lineage_logger()
    if logger and results:
        logger.log_context_injection(
            query=query,
            decisions_surfaced=[r.title for r in results],
            agent="mcp-client",
            tool_name="query_decisions",
        )

    # Déjà Vu check — zero LLM calls, purely graph similarity
    rejections = await client.check_rejected_alternatives(query=query, project=project)

    if not results and not rejections:
        return f"No relevant decisions found for query: {query!r} in project {project!r}."

    lines = [f"## Decisions relevant to: {query!r}\n"]

    if rejections:
        lines.append("⚠️  DÉJÀ VU WARNING")
        lines.append("=" * 40)
        lines.append(
            "This query resembles previously-rejected alternatives. "
            "Review before proceeding:\n"
        )
        for r in rejections:
            lines.append(f"**Decision:** {r.decision_title}")
            lines.append(f"**Reason rejected:** {r.rationale[:300]}")
            lines.append(f"**Confidence:** {r.confidence:.2f}")
            lines.append("")
        lines.append("=" * 40 + "\n")

    for i, r in enumerate(results, 1):
        lines.append(f"### {i}. {r.title}")
        lines.append(r.content)
        if r.excerpt and r.excerpt != r.content:
            lines.append(f"\n*Excerpt:* {r.excerpt}")
        lines.append("")
    return "\n".join(lines) + _time_saved_footer()


@mcp.tool(structured_output=False)
async def add_decision(
    title: str,
    content: str = "",
    rationale: str = "",
    made_by: str = "",
    project: str = "smm-sync",
    constraints: list[str] = [],
    alternatives: list[str] = [],
    decision_type: str = "technical",
    confidence: float | None = None,
    session_token: str = "",
    # Field aliases — agents often send these instead of the canonical names
    description: str = "",
    type: str = "",
) -> dict:
    """Record an architectural, technical, product, or constraint decision.

    Call whenever choosing between two or more alternatives. Required fields:
    title, description, type, confidence.

    Args:
        title: Short title of the decision.
        content: Full description of what was decided. Alias: description.
        rationale: Why this decision was made.
        made_by: Who made this decision (defaults to 'agent').
        project: Project name (default: smm-sync).
        constraints: Known constraints imposed by this decision.
        alternatives: Alternatives that were considered.
        decision_type: One of 'architectural', 'technical', 'product', 'constraint'. Alias: type.
        confidence: Confidence score (0.0–1.0).
        session_token: Session token from get_project_context. Stored as session_id
                       in decisions.jsonl and compliance_lineage.jsonl.
        description: Alias for content.
        type: Alias for decision_type.

    Returns:
        Dict with keys: success (bool), decision_id (str) or error (str).
    """
    global _context_loaded
    if not _context_loaded:
        return _SESSION_NOT_INIT_ERROR

    # Resolve field aliases before any validation
    if description and not content:
        content = description
    if type and decision_type == "technical":
        # "technical" is the default; if an explicit alias was provided, prefer it
        decision_type = type
    if not made_by:
        made_by = "agent"

    if confidence is not None and not (0.0 <= confidence <= 1.0):
        return {"success": False, "error": f"Confidence must be between 0.0 and 1.0, got {confidence!r}"}

    import os as _os
    _use_local = not _os.environ.get("ANTHROPIC_API_KEY", "").strip()

    # Kuzu write — optional; lock contention or unavailability must not block JSONL
    decision_id = str(uuid4())  # fallback ID used when Kuzu is unavailable
    client = _get_graph_client()
    if client is None:
        logger.warning("[add_decision] Context graph unavailable, skipping Kuzu write.")
    else:
        try:
            if _use_local:
                decision_id = await client.add_decision_local(
                    title=title,
                    content=content,
                    rationale=rationale,
                    made_by=made_by,
                    project=project,
                    constraints=list(constraints),
                    alternatives=list(alternatives),
                    decision_type=decision_type,
                )
            else:
                decision_id = await client.add_decision(
                    title=title,
                    content=content,
                    rationale=rationale,
                    made_by=made_by,
                    project=project,
                    constraints=list(constraints),
                    alternatives=list(alternatives),
                    decision_type=decision_type,
                )
        except Exception as e:
            logger.warning(f"[add_decision] Kuzu write failed (lock contention?): {e}")

    # JSONL write — critical path; always runs regardless of Kuzu state.
    # Pass smm_dir explicitly — the MCP server CWD is not the project root
    # so relying on _find_smm_dir() walking up from CWD will fail silently.
    try:
        from smm_sync.jsonl_writer import write_decision as _write_decision
        _write_decision({
            "title": title,
            "rationale": rationale or content,
            "type": decision_type,
            "confidence": confidence,
            "alternatives": list(alternatives),
            "constraints": list(constraints),
            "made_by": made_by,
            "project": project,
            "source": "mcp",
            "session_id": session_token or _session_token,
        }, project=project, smm_dir=_get_smm_dir())
    except Exception as _e:
        logger.warning(f"[add_decision] JSONL write failed: {_e}")

    return {"success": True, "decision_id": decision_id}


def _load_decision_rationales(smm_dir: Path) -> dict:
    """Return {title: rationale} from decisions.jsonl for contradiction display."""
    rationale_map: dict = {}
    decisions_path = smm_dir / "decisions.jsonl"
    if not decisions_path.exists():
        return rationale_map
    try:
        for line in decisions_path.read_text(encoding="utf-8").splitlines():
            line = line.strip()
            if not line:
                continue
            try:
                d = json.loads(line)
                title = d.get("title", "")
                rationale = d.get("rationale", "") or d.get("content", "")
                if title:
                    rationale_map[title] = rationale or ""
            except Exception:
                pass
    except Exception:
        pass
    return rationale_map


def _format_contradiction_lines(active_contras: list, rationale_map: dict) -> list:
    """Format unresolved contradictions with full context and resolve commands."""
    out: list = []
    out.append(f"⚠ UNRESOLVED CONTRADICTIONS ({len(active_contras)}):")
    out.append("")
    for i, ac in enumerate(active_contras, 1):
        da = ac.get("decision_a", "")
        db = ac.get("decision_b", "")
        reason = ac.get("reason", ac.get("explanation", ""))
        cid = ac.get("uuid", ac.get("id", ""))
        rationale_a = rationale_map.get(da, "") or "(no rationale recorded)"
        rationale_b = rationale_map.get(db, "") or "(no rationale recorded)"
        if len(rationale_a) > 120:
            rationale_a = rationale_a[:117] + "..."
        if len(rationale_b) > 120:
            rationale_b = rationale_b[:117] + "..."
        out.append(f"{i}. [ID: {cid}] '{da}' ↔ '{db}'")
        if reason:
            out.append(f"   Reason: {reason}")
        out.append(f"   Option A rationale: {rationale_a}")
        out.append(f"   Option B rationale: {rationale_b}")
        out.append(f"   → resolve_contradiction(id='{cid}', winner='a') — keep {da}")
        out.append(f"   → resolve_contradiction(id='{cid}', winner='b') — keep {db}")
        out.append(f"   → resolve_contradiction(id='{cid}', winner='dismiss') — dismiss this contradiction")
        out.append("")
    out.append("These require DEVELOPER confirmation to resolve. Present the options and ask the developer to confirm before calling resolve_contradiction.")
    out.append("")
    return out


