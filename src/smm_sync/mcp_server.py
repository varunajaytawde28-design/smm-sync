@mcp.tool(structured_output=False)
async def check_constraints(
    proposed_action: str,
    project: str = "smm-sync",
    session_id: str = "",
) -> dict:
    """Check if a proposed action violates any known project constraints.

    Call this before making significant architectural changes, adding dependencies,
    or modifying core systems.

    Args:
        proposed_action: What you are about to do.
                        e.g. "replace Kuzu with FalkorDB"
                        e.g. "add LWW CRDT back to state.py"
                        e.g. "expose raw MCP without gateway"
        project: Project name (default: smm-sync).
        session_id: Optional session identifier (used for kill check).

    Returns:
        Dict with keys:
            conflicts (list[str]): Decisions this action directly conflicts with.
            warnings (list[str]): Decisions worth reviewing before proceeding.
            clear (bool): True if no conflicts or warnings found.
    """
    global _context_loaded
    if not _context_loaded:
        return _SESSION_NOT_INIT_ERROR
    if session_id and _is_session_killed(session_id):
        return {"conflicts": [_KILLED_MESSAGE], "warnings": [], "clear": False}
    client = _get_graph_client()
    if client is None:
        return {
            "conflicts": [],
            "warnings": ["Context graph unavailable — cannot check constraints."],
            "clear": False,
        }

    try:
        # Search for decisions related to the proposed action
        results = await client.search_context(
            query=proposed_action,
            project=project,
            limit=10,
        )
    except Exception as e:
        return {
            "conflicts": [],
            "warnings": [f"Graph search failed: {e}"],
            "clear": False,
        }

    # Log to compliance lineage
    logger = _get_lineage_logger()
    if logger and results:
        logger.log_context_injection(
            query=proposed_action,
            decisions_surfaced=[r.title for r in results],
            agent="mcp-client",
            tool_name="check_constraints",
        )

    # Heuristic: high relevance scores indicate potential conflicts;
    # lower scores are warnings. Graphiti doesn't expose raw scores on
    # basic search so we use content-based keyword matching as a proxy.
    conflict_keywords = [
        "do not", "never", "must not", "rejected", "prohibited",
        "not expose", "not allowed", "not safe", "forbidden",
    ]
    warning_keywords = [
        "constraint", "caution", "warning", "danger", "only",
        "requires", "must", "before", "check",
    ]

    conflicts = []
    warnings = []

    for r in results:
        content_lower = r.content.lower()
        is_conflict = any(kw in content_lower for kw in conflict_keywords)
        is_warning = any(kw in content_lower for kw in warning_keywords)

        summary = r.title or r.content[:80]
        if is_conflict:
            conflicts.append(summary)
        elif is_warning:
            warnings.append(summary)

    # Auth check — warn if GitHub sync is broken (Fix 3)
    github_ok = await _check_github_auth()
    if not github_ok:
        warnings.append(
            "\u26a0\ufe0f WARNING: GitHub sync is broken. "
            "Context may be stale. "
            "Check GITHUB_TOKEN environment variable."
        )

    return {
        "conflicts": conflicts,
        "warnings": warnings,
        "clear": len(conflicts) == 0 and len(warnings) == 0,
    }


@mcp.tool(structured_output=False)
async def get_decision_timeline(
    topic: str,
    project: str = "smm-sync",
) -> str:
    """Get the chronological history of decisions related to a topic.

    Shows how team thinking evolved, including superseded decisions.

    Call this to understand WHY the codebase evolved the way it did.
    Shows the full audit trail including decisions that were later
    reversed or superseded.

    Research basis: EVOKG (MIT CSAIL 2025) — temporal graphs that track
    superseding relationships outperform static graphs by 23.3%.

    Args:
        topic: Natural language topic (e.g. "state management", "database choice").
        project: Project name (default: smm-sync).

    Returns:
        Chronological timeline of all decisions related to the topic,
        with superseded decisions marked but not hidden.
    """
    global _context_loaded
    if not _context_loaded:
        return _SESSION_NOT_INIT_ERROR
    client = _get_graph_client()
    if client is None:
        return "Context graph unavailable. Run `smm seed-graph` to populate it."

    try:
        timeline = await client.get_decision_timeline(topic=topic, project=project)
    except Exception as e:
        return f"Timeline retrieval failed: {e}"

    if not timeline:
        return f"No decisions found for topic: {topic!r} in project {project!r}."

    lines = [f"## Decision Timeline: {topic!r}\n", f"{len(timeline)} decisions found.\n"]
    for i, entry in enumerate(timeline, 1):
        status = "✅ ACTIVE" if entry.get("valid") else "⚠️  SUPERSEDED"
        ts = entry.get("created_at") or "unknown time"
        lines.append(f"### {i}. [{status}] {entry['title']}")
        lines.append(f"*{ts}*")
        lines.append(entry.get("content", ""))
        if entry.get("superseded_note"):
            lines.append(f"\n> {entry['superseded_note']}")
        lines.append("")

    return "\n".join(lines)


@mcp.tool(structured_output=False)
async def get_compliance_lineage(
    session_id: str | None = None,
    decision_title: str | None = None,
) -> str:
    """Get the compliance audit trail.

    If session_id provided: shows all context that was injected into that
    AI coding session — exactly what the AI knew during that session.

    If decision_title provided: shows all times this decision was surfaced
    to an AI agent — the full injection history.

    Required for EU AI Act compliance and SOC 2 AI governance audits.

    Args:
        session_id: Optional session identifier to filter by.
        decision_title: Optional decision title to filter by.

    Returns:
        Formatted audit trail string.
    """
    global _context_loaded
    if not _context_loaded:
        return _SESSION_NOT_INIT_ERROR
    logger = _get_lineage_logger()
    if logger is None:
        return "Compliance lineage logger unavailable."

    if session_id:
        entries = logger.get_session_lineage(session_id)
        header = f"## Compliance Lineage for Session: {session_id}"
    elif decision_title:
        entries = logger.get_decision_lineage(decision_title)
        header = f"## Compliance Lineage for Decision: {decision_title!r}"
    else:
        entries = logger.get_all_entries()
        header = "## Compliance Lineage (all entries)"

    if not entries:
        return f"{header}\n\nNo entries found."

    lines = [header, f"\n{len(entries)} entries.\n"]
    for e in entries[:50]:  # Cap at 50 for display
        ts = e.get("timestamp", "")[:19]
        tool = e.get("tool_name") or e.get("event_type", "")
        decisions = e.get("decisions_surfaced", [])
        if decisions:
            lines.append(f"- {ts} | {tool} | surfaced: {', '.join(decisions[:3])}")
        else:
            lines.append(f"- {ts} | {e.get('event_type', '')} | {e.get('decision_title', '')}")

    if len(entries) > 50:
        lines.append(f"\n... and {len(entries) - 50} more entries.")

    return "\n".join(lines)


@mcp.tool(structured_output=False)
async def add_constraint(
    constraint: str,
    scope_keywords: list[str],
    rationale: str,
    project: str = "smm-sync",
) -> dict:
    """Register a non-negotiable project constraint.

    Constraints are different from decisions — they are rules that must never
    be violated and automatically surface in ANY query related to their scope
    keywords.

    Example:
        add_constraint(
            constraint="Never expose raw MCP to enterprise customers",
            scope_keywords=["MCP", "enterprise", "security"],
            rationale="6 fatal security flaws in raw MCP protocol"
        )

    Args:
        constraint: The constraint rule in one clear sentence.
        scope_keywords: Keywords that trigger this constraint to surface.
        rationale: Why this constraint exists.
        project: Project name (default: smm-sync).

    Returns:
        Dict with keys: success (bool), constraint_id (str) or error (str).
    """
    global _context_loaded
    if not _context_loaded:
        return _SESSION_NOT_INIT_ERROR
    client = _get_graph_client()
    if client is None:
        return {"success": False, "error": "Context graph unavailable."}

    try:
        constraint_id = await client.add_decision(
            title=f"[CONSTRAINT] {constraint[:80]}",
            content=constraint,
            rationale=rationale,
            made_by="manual-constraint",
            project=project,
            constraints=[constraint],
            alternatives=[],
            decision_type="architectural",
            source_type="manual",
        )
        return {"success": True, "constraint_id": constraint_id}
    except Exception as e:
        return {"success": False, "error": str(e)}


@mcp.tool(structured_output=False)
async def complete_session(session_token: str) -> str:
    """Close the current work session and summarise decisions captured.

    Must be called after all implementation work is done and before committing.
    Verifies how many decisions were tagged with this session_token and lists
    the files modified during the session (from git diff and git ls-files for new untracked files).

    Args:
        session_token: The UUID returned by get_project_context at session start.

    Returns:
        'Session {token} captured {N} decisions across {M} files.
         Review these modified files for missed decisions: [list]'
    """
    global _context_loaded
    if not _context_loaded:
        return _SESSION_NOT_INIT_ERROR
    if not session_token:
        return "session_token is required. Pass the token returned by get_project_context."

    smm_dir = _get_smm_dir()
    decisions_path = smm_dir / "decisions.jsonl"

    # Count decisions tagged with this session
    n_decisions = 0
    if decisions_path.exists():
        for _raw in decisions_path.read_text(encoding="utf-8").splitlines():
            _raw = _raw.strip()
            if not _raw:
                continue
            try:
                row = json.loads(_raw)
                if row.get("session_id") == session_token:
                    n_decisions += 1
            except Exception:
                pass

    # Collect modified files from git (staged + unstaged tracked files, plus new untracked files)
    modified_files: set[str] = set()
    try:
        for _git_args in (["git", "diff", "--name-only", "HEAD"],
                          ["git", "diff", "--cached", "--name-only"],
                          ["git", "ls-files", "--others", "--exclude-standard"]):
            _r = subprocess.run(_git_args, capture_output=True, text=True, timeout=10)
            for _f in _r.stdout.strip().splitlines():
                if _f.strip():
                    modified_files.add(_f.strip())
    except Exception:
        pass

    m_files = len(modified_files)
    files_list = sorted(modified_files)

    # Log session close to compliance lineage
    _ll = _get_lineage_logger()
    if _ll:
        _ll.log_context_injection(
            query=f"complete_session:{session_token}",
            decisions_surfaced=[],
            agent="mcp-client",
            session_id=session_token,
            tool_name="complete_session",
        )

    files_str = ", ".join(files_list) if files_list else "none"

    # Fire-and-forget: run contradiction analysis in background so the next
    # session's get_project_context picks up any newly detected contradictions.
    subprocess.Popen(
        ["smm", "check", "--quiet"],
        stdout=subprocess.DEVNULL,
        stderr=subprocess.DEVNULL,
        cwd=str(smm_dir.parent),
    )

    return (
        f"Session {session_token} captured {n_decisions} decisions across {m_files} files. "
        f"Review these modified files for missed decisions: [{files_str}]"
    )


def _read_board(smm_dir: Path) -> list[dict]:
    """Read board items from .smm/board.json (stored as {"items": [...]}).

    Args:
        smm_dir: Path to .smm directory.

    Returns:
        List of board item dicts. Returns [] if file missing or malformed.
    """
    board_path = smm_dir / "board.json"
    if not board_path.exists():
        return []
    try:
        data = json.loads(board_path.read_text(encoding="utf-8"))
        if isinstance(data, list):
            return data
        return data.get("items", [])
    except Exception:
        return []


def _write_board(smm_dir: Path, items: list[dict]) -> None:
    """Write board items to .smm/board.json atomically ({"items": [...]}).

    Args:
        smm_dir: Path to .smm directory.
        items: List of board item dicts to persist.
    """
    smm_dir.mkdir(parents=True, exist_ok=True)
    board_path = smm_dir / "board.json"
    tmp = board_path.with_suffix(".tmp")
    tmp.write_text(json.dumps({"items": items}, indent=2), encoding="utf-8")
    tmp.replace(board_path)


@mcp.tool(structured_output=False)
async def get_path_context(
    file_path: str,
    project: str = "smm-sync",
) -> str:
    """Get just-in-time context rules relevant to the file you are about to edit.

    Call this before editing a file to surface constraints and decisions that
    apply specifically to that part of the codebase.

    Args:
        file_path: Relative or absolute path to the file being edited.
                   e.g. "src/smm_sync/mcp_server.py"
        project: Project name (default: smm-sync).

    Returns:
        Formatted string of relevant rules and constraints (up to 3).
        Returns a "no specific rules" message if the graph is empty or unavailable.
    """
    global _context_loaded
    if not _context_loaded:
        return _SESSION_NOT_INIT_ERROR
    client = _get_graph_client()
    if client is None:
        return "Context graph unavailable."

    results = await client.get_path_context(file_path=file_path, project=project)
    if not results:
        return f"No specific rules found for path: {file_path!r}"

    lines = [f"## JIT Context for `{file_path}`\n"]
    for r in results:
        lines.append(f"**{r.title}**")
        lines.append(r.excerpt or r.content[:300])
        lines.append("")
    return "\n".join(lines)


@mcp.tool(structured_output=False)
async def get_board_items(
    status: str = "",
) -> str:
    """Read the decision board from .smm/board.json.

    The board tracks decisions, tasks, and blockers in a kanban-style layout
    with statuses: backlog, in_progress, done.

    Args:
        status: Optional filter — one of "backlog", "in_progress", "done".
                Empty string returns all items.

    Returns:
        Formatted markdown list of board items.
    """
    global _context_loaded
    if not _context_loaded:
        return _SESSION_NOT_INIT_ERROR
    smm_dir = _get_smm_dir()
    items = _read_board(smm_dir)
    if not items:
        return "Board is empty. Use `update_board_item` to add items."

    if status and status != "all":
        items = [i for i in items if i.get("status") == status]

    if not items:
        return f"No board items with status: {status!r}"

    lines = ["## Decision Board\n"]
    for item in items:
        st = item.get("status", "backlog")
        icon = {"backlog": "○", "in_progress": "◑", "done": "●"}.get(st, "○")
        lines.append(f"{icon} **{item.get('title', '?')}** [{st}]")
        if item.get("description"):
            lines.append(f"  {item['description'][:200]}")
        lines.append(f"  id: {item.get('id', '?')}")
        lines.append("")
    return "\n".join(lines)


@mcp.tool(structured_output=False)
async def update_board_item(
    title: str = "",
    status: str = "backlog",
    description: str = "",
    item_id: str = "",
) -> dict:
    """Create or update an item on the decision board.

    Board items track active decisions, open questions, and blockers.
    Status flow: backlog → in_progress → done.

    Args:
        title: Short title for the board item.
        status: One of "backlog", "in_progress", "done". Default: backlog.
        description: Optional longer description or acceptance criteria.
        item_id: If provided, update existing item with this id.
                 If empty, create a new item.

    Returns:
        Dict with keys: success (bool), id (str), action ("created" or "updated").
    """
    global _context_loaded
    if not _context_loaded:
        return _SESSION_NOT_INIT_ERROR
    import uuid
    from datetime import datetime, timezone

    smm_dir = _get_smm_dir()
    items = _read_board(smm_dir)

    if item_id:
        # Update existing
        for item in items:
            if item.get("id") == item_id:
                if title:
                    item["title"] = title
                item["status"] = status
                if description:
                    item["description"] = description
                item["updated_at"] = datetime.now(timezone.utc).isoformat()
                _write_board(smm_dir, items)
                return {"success": True, "id": item_id, "action": "updated"}
        return {"success": False, "error": f"Item {item_id!r} not found."}

    # Create new
    new_id = str(uuid.uuid4())[:8]
    now = datetime.now(timezone.utc).isoformat()
    items.append({
        "id": new_id,
        "title": title,
        "status": status,
        "description": description,
        "created_at": now,
        "updated_at": now,
    })
    _write_board(smm_dir, items)
    return {"success": True, "id": new_id, "action": "created"}


@mcp.tool(structured_output=False)
async def check_contradictions(project: str = "smm-sync") -> dict:
    """Run smm check inline to sync new decisions and detect contradictions.

    Syncs any unprocessed decisions from JSONL into the graph, runs contradiction
    detection, and returns a summary of what was found. This is the same operation
    as running ``smm check`` from the command line.

    Use this after a burst of ``add_decision`` calls to surface conflicts before
    proceeding with implementation.

    Args:
        project: Project name (default: smm-sync).

    Returns:
        Dict with keys:
            synced (int): Number of new decisions synced into the graph.
            contradictions_found (int): Number of new contradictions detected.
            unresolved (list[dict]): Currently unresolved contradictions
                (each has decision_a, decision_b, reason).
            dirty (bool): True if the graph was updated (smm compile should run).
            message (str): Human-readable summary.
    """
    global _context_loaded
    if not _context_loaded:
        return _SESSION_NOT_INIT_ERROR

    import asyncio as _asyncio_cc

    smm_dir = _get_smm_dir()

    # Run smm check in a thread so we don't block the event loop
    loop = _asyncio_cc.get_event_loop()
    proc_result = await loop.run_in_executor(
        None,
        lambda: subprocess.run(
            ["smm", "check", "--quiet"],
            capture_output=True,
            text=True,
            timeout=120,
            cwd=str(smm_dir.parent),
        ),
    )

    dirty = (smm_dir / ".check_dirty").exists()

    # Read unresolved contradictions
    unresolved: list[dict] = []
    _unresolved_full: list[dict] = []
    contra_path = smm_dir / "contradictions.jsonl"
    if contra_path.exists():
        for _ln in contra_path.read_text(encoding="utf-8").splitlines():
            _ln = _ln.strip()
            if not _ln:
                continue
            try:
                _c = json.loads(_ln)
                if _c.get("status") not in ("resolved", "dismissed", "ignored") and not _c.get("resolved"):
                    unresolved.append({
                        "id": _c.get("uuid", _c.get("id", "")),
                        "decision_a": _c.get("decision_a", ""),
                        "decision_b": _c.get("decision_b", ""),
                        "reason": _c.get("reason", ""),
                    })
                    _unresolved_full.append(_c)
            except Exception:
                pass

    if proc_result.returncode != 0:
        return {
            "synced": 0,
            "contradictions_found": 0,
            "unresolved": unresolved,
            "dirty": dirty,
            "message": f"smm check failed: {proc_result.stderr.strip()[:200]}",
        }

    # Parse output to extract counts (best-effort; quiet mode may produce nothing)
    synced = 0
    contra_found = 0
    _out = proc_result.stdout.strip()
    import re as _re_cc
    _m = _re_cc.search(r"Checked (\d+) new decision", _out)
    if _m:
        synced = int(_m.group(1))
    _m2 = _re_cc.search(r"Found (\d+) contradiction", _out)
    if _m2:
        contra_found = int(_m2.group(1))

    if dirty:
        msg = (
            f"Synced {synced} decision(s), found {contra_found} new contradiction(s). "
            f"{len(unresolved)} unresolved total. Graph updated."
        )
    else:
        msg = f"No new decisions to process. {len(unresolved)} unresolved contradiction(s)."

    _rationale_map_cc = _load_decision_rationales(smm_dir)
    _display = (
        "\n".join(_format_contradiction_lines(_unresolved_full, _rationale_map_cc))
        if _unresolved_full else ""
    )

    return {
        "synced": synced,
        "contradictions_found": contra_found,
        "unresolved": unresolved,
        "dirty": dirty,
        "message": msg,
        "display": _display,
    }


@mcp.tool(structured_output=False)
async def resolve_contradiction(
    id: str,
    winner: str,
    note: str = "",
    confirmation: str = "",
) -> str:
    """Resolves a contradiction. REQUIRES explicit developer confirmation. Before calling, you MUST ask the developer which option to keep and have them type YES to confirm. Never auto-resolve based on PRD context alone.

    Flow:
      1. Show the developer the contradiction with both options and rationales.
      2. Ask: 'Which do you want to keep? A, B, or dismiss?'
      3. After they answer, ask: 'To confirm, type YES to proceed.'
      4. Pass their exact typed confirmation as `confirmation`.

    Records the resolution in contradictions.jsonl and writes an audit event
    to compliance_lineage.jsonl with the SHA-256 hash chain.

    Args:
        id: Contradiction UUID (from get_project_context or check_contradictions).
        winner: Which decision wins — 'a' (keep decision_a), 'b' (keep decision_b),
                or 'dismiss' (mark as not a real contradiction, no action taken).
        note: Optional developer comment on why this choice was made.
        confirmation: Must be exactly 'YES' (typed by the developer). Required.

    Returns:
        Confirmation with resolution outcome and 'Resolved N/M contradictions. N remaining.'
    """
    global _context_loaded
    if not _context_loaded:
        return (
            "⚠️ SESSION NOT INITIALIZED: Call get_project_context before resolve_contradiction."
        )

    if confirmation != "YES":
        raise ValueError(
            "Human confirmation required. Ask the developer to type YES to confirm this resolution."
        )

    if winner not in ("a", "b", "dismiss"):
        return f"Invalid winner={winner!r}. Must be 'a', 'b', or 'dismiss'."

    smm_dir = _get_smm_dir()
    contra_path = smm_dir / "contradictions.jsonl"

    if not contra_path.exists():
        return f"No contradictions.jsonl found at {contra_path}."

    # Load all records; find target by UUID
    records: list = []
    target: dict | None = None
    try:
        for _ln in contra_path.read_text(encoding="utf-8").splitlines():
            _ln = _ln.strip()
            if not _ln:
                continue
            try:
                r = json.loads(_ln)
                records.append(r)
                if (r.get("uuid") == id or r.get("id") == id) and target is None:
                    target = r
            except Exception:
                pass
    except Exception as e:
        return f"Failed to read contradictions.jsonl: {e}"

    if target is None:
        return f"Contradiction ID '{id}' not found."

    da = target.get("decision_a", "")
    db = target.get("decision_b", "")

    from datetime import datetime as _dt_rc, timezone as _tz_rc
    now_str = _dt_rc.now(_tz_rc.utc).isoformat()

    # Mutate the target record (it's in `records` by reference)
    if winner == "dismiss":
        target["status"] = "dismissed"
        target["resolved"] = False
        target["dismissed_at"] = now_str
        if note:
            target["dismiss_note"] = note
        event_type = "contradiction_dismissed"
    else:
        winning_title = da if winner == "a" else db
        losing_title = db if winner == "a" else da
        target["status"] = "resolved"
        target["resolved"] = True
        target["resolved_winner"] = winning_title
        target["resolved_at"] = now_str
        if note:
            target["resolution_note"] = note
        event_type = "contradiction_resolved"

    # Rewrite contradictions.jsonl atomically
    try:
        tmp_path = contra_path.with_suffix(".tmp")
        with open(tmp_path, "w", encoding="utf-8") as fh:
            for r in records:
                fh.write(json.dumps(r, ensure_ascii=False) + "\n")
        tmp_path.replace(contra_path)
    except Exception as e:
        return f"Failed to write contradictions.jsonl: {e}"

    # Write compliance_lineage event with hash chain
    try:
        from smm_sync.jsonl_writer import _write_audit_hashed as _wah
        import uuid as _uuid_rc
        lineage_path = smm_dir / "compliance_lineage.jsonl"
        _wah(lineage_path, {
            "entry_id": str(_uuid_rc.uuid4()),
            "timestamp": now_str,
            "event_type": event_type,
            "contradiction_id": id,
            "decision_a": da,
            "decision_b": db,
            "winner": winner,
            "note": note or None,
            "session_token": _session_token or None,
        })
    except Exception as e:
        logger.warning(f"Failed to write compliance lineage for contradiction resolution: {e}")

    # Count remaining unresolved after update
    remaining = sum(
        1 for r in records
        if r.get("status") not in ("resolved", "dismissed", "ignored") and not r.get("resolved")
    )
    total = len(records)
    actioned = total - remaining

    if winner == "dismiss":
        outcome = f"Dismissed: '{da}' ↔ '{db}' — no action taken."
    else:
        outcome = f"Resolved: keeping '{winning_title}', superseding '{losing_title}'."  # type: ignore[possibly-undefined]

    suffix = (
        "\n\nContinue resolving? Call check_contradictions to see the remaining list."
        if remaining > 0 else "\n\n✅ All contradictions resolved."
    )
    return f"{outcome}\nResolved {actioned}/{total} contradictions. {remaining} remaining.{suffix}"


def _configure_mcp_stdio():
    """Redirect all stdout to stderr for MCP stdio mode.

    MCP uses stdout exclusively for JSON-RPC messages.
    Any non-JSON output on stdout corrupts the protocol.
    This must be called before mcp.run().

    After this call:
    - print() goes to stderr (visible in terminal)
    - mcp.run() has clean stdout for JSON-RPC
    - Logging goes to stderr
    """
    import logging
    logging.basicConfig(
        stream=sys.stderr,
        level=logging.WARNING,
        format='%(levelname)s: %(message)s'
    )


def run_server(smm_dir: Path) -> None:
    """Start the MCP server bound to the given .smm directory.

    Also starts the dashboard on the configured port in a background daemon thread so
    the MCP server and dashboard share one Kuzu connection (no file-lock
    contention when CLI writes go through the dashboard HTTP API).

    Runs in foreground (blocking). Prints the transport info on start.

    Args:
        smm_dir: Path to the .smm directory for this project.
    """
    global _smm_dir, _project_hash
    _smm_dir = smm_dir
    _project_hash = hashlib.sha1(str(Path.cwd().resolve()).encode()).hexdigest()[:8]
    # Clean slate: delete any stale lock from a previously killed session.
    Path(f"/tmp/smm-session-{_project_hash}.lock").unlink(missing_ok=True)
    atexit.register(lambda: Path(f"/tmp/smm-session-{_project_hash}.lock").unlink(missing_ok=True))

    # Root-Cause-1 fix: dashboard runs in same process → shares _graph_client
    # singleton → single Kuzu writer → no "Could not set lock on file" errors.
    import threading

    def _start_dashboard() -> None:
        try:
            from smm_sync.dashboard import run_dashboard  # type: ignore[attr-defined]
            run_dashboard(host="127.0.0.1", port=DASHBOARD_PORT)
        except Exception as exc:  # noqa: BLE001
            import sys as _sys
            print(f"[smm-sync] dashboard failed to start: {exc}", file=_sys.stderr)

    threading.Thread(target=_start_dashboard, name="smm-dashboard", daemon=True).start()

    _configure_mcp_stdio()  # MUST be before mcp.run()
    mcp.run()
